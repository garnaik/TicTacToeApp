package com.garnaik;

/**
 * 
 * @author Garnaik
 * This is the class that hold the User enter value of row, column and BoxValue
 *
 */
public class EnterValue {
	private int column;
	private int row;
	private String boxValue;
	public int getColumn() {
		return column;
	}
	public void setColumn(int column) {
		this.column = column;
	}
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}
	public String getBoxValue() {
		return boxValue;
	}
	public void setBoxValue(String boxValue) {
		this.boxValue = boxValue;
	}
	

}
